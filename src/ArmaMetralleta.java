/**
 * Clase que define los atributos de una arma metralleta.
 */
public class ArmaMetralleta extends Arma {

    /**
     * Constuye una arma metralleta.
     */
    public ArmaMetralleta() {
        ataque = 100;
        defensa = 0;
        velocidad = 0;
        costo = 100;
        tipo = "Metralleta.";
    }
}
