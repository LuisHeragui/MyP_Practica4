/**
 * Clase que define los atributos de una llanta simple.
 */
public class LlantaSimple extends Llanta {

    /**
     * Constuye una llanta simple.
     */
    public LlantaSimple() {
        ataque = 0;
        defensa = 0;
        velocidad = 50;
        costo = 50;
        tipo = "Simples.";
    }
}
